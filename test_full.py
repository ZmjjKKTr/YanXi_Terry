from pipeline import Pipeline
from config import set_user

set_user("zmjjkk")

p = Pipeline()
passed = 0
failed = 0


def test(name: str, session_id: str, turns: list[str], expected_behaviors: list[str]):
    """turns: 多轮输入列表; expected_behaviors: 每轮回复中应包含的关键特征"""
    global passed, failed
    print(f"\n{'='*60}")
    print(f"  测试: {name}")
    print(f"{'='*60}")

    ok = True
    for i, (user_input, expected) in enumerate(zip(turns, expected_behaviors), 1):
        resp = p.process(session_id, user_input, "zmjjkk")
        print(f"\n  [轮{i}] 来电: {user_input}")
        print(f"  [轮{i}] 回复: {resp}")

        # 检查是否包含期望特征
        if isinstance(expected, list):
            match = any(e in resp for e in expected)
            expected_str = " 或 ".join(f'"{e}"' for e in expected)
        else:
            match = expected in resp
            expected_str = f'"{expected}"'

        if match:
            print(f"  [OK] 包含 {expected_str}")
        else:
            print(f"  [FAIL] 期望包含 {expected_str}，实际未匹配")
            ok = False

    if ok:
        passed += 1
        print(f"\n  >>> 测试通过 <<<")
    else:
        failed += 1
        print(f"\n  >>> 测试失败 <<<")


